public class RegisterForm {

    private String age;
    private String height;
    private String weight;
    private String username;
    private String password;
    private String weightSelect;
    private String heightSelect;
    private String exerciseLevel;
    private String male;
    private String female;

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getWeightSelect() {
        return weightSelect;
    }

    public void setWeightSelect(String weightSelect) {
        this.weightSelect = weightSelect;
    }

    public String getHeightSelect() {
        return heightSelect;
    }

    public void setHeightSelect(String heightSelect) {
        this.heightSelect = heightSelect;
    }

    public String getExerciseLevel() {
        return exerciseLevel;
    }

    public void setExerciseLevel(String exerciseLevel) {
        this.exerciseLevel = exerciseLevel;
    }

    public String getMale() {
        return male;
    }

    public void setMale(String male) {
        this.male = male;
    }

    public String getFemale() {
        return female;
    }

    public void setFemale(String female) {
        this.female = female;
    }
}
